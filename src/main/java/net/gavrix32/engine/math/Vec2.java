package net.gavrix32.engine.math;

public class Vec2 {
    public float x, y;

    public Vec2() {
        this.x = 0;
        this.y = 0;
    }

    public Vec2(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public Vec2(float value) {
        this.x = value;
        this.y = value;
    }
}
